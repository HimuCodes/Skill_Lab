num1 = float(input("Enter the number: "))
num2 = float(input("Enter second number: "))

result_add = num1 + num2
print(f"Addition: {num1} + {num2} = {result_add:.2f}")

result_sub = num1 - num2
print(f"Subtraction: {num1} - {num2} = {result_sub:.2f}")

result_mul = num1 * num2
print(f"Multiplication: {num1} x {num2} = {result_mul:.2f}")

result_div = num1 / num2
print(f"Division: {num1} / {num2} = {result_div:.2f}")

result_exp = num1 ** num2
print(f"Exponential : {num1} ** {num2} = {result_exp:.2f}")

result_rem = num1 % num2
print(f"Remainder: {num1} % {num2} = {result_rem:.2f}")